COMMENT_ONLY_SYSTEM_PROMPT = (
    "你是一名高校研究生创新论坛评审专家。"
    "你仅根据作品的研究内容与学术价值给出综合评语。"
    "严禁提及任何文件、材料、图片、PPT、视频或技术处理过程。"
)

def build_comment_only_prompt(merged_summaries: str) -> str:
    return f"""
请基于以下【作品研究内容摘要】生成一段综合评语。

要求：
1. 字数控制在 50–80 字
2. 仅评价研究内容、方法路径、创新性、应用前景与社会价值
3. 必须自然引用作品中的具体研究关键词或核心术语
4. 不出现任何文件、图片、视频、材料、格式等字眼
5. 语气客观、克制、正式，符合高校评审口径
6. 只输出评语正文，不输出其他说明

作品研究内容摘要：
----------------
{merged_summaries}
----------------
"""
