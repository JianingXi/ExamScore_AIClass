# =========================================================
# Stage 1：文件级压缩理解（允许感知来源，但不输出）
# =========================================================

STAGE1_SYSTEM_PROMPT = (
    "你是一名高校学术评审助手，负责对学术作品内容进行高度压缩理解。"
    "你只关注作品的研究内容本身，而不是其载体或呈现形式。"
)

def build_stage1_user_prompt(material: str) -> str:
    return f"""
请对以下【作品研究内容】进行高度压缩理解。

要求：
1. 提炼 3–5 条与研究内容直接相关的关键信息
2. 仅关注：研究主题、研究对象、方法路径、核心结论、创新点
3. 不提及任何文件、图片、PPT、视频、格式、载体、展示方式
4. 不进行任何评分或评价
5. 使用 JSON 输出，格式严格如下：

{{
  "key_points": ["研究内容要点"],
  "academic_keywords": ["作品中的具体学术关键词"],
  "innovation_signal": "low | moderate | high"
}}

作品研究内容：
----------------
{material}
----------------
"""



# =========================================================
# Stage 2：作品级综合评分（评分项强锁定版）
# =========================================================

STAGE2_SYSTEM_PROMPT = (
    "你是一名高校研究生创新论坛的正式评审专家。"
    "你必须严格按照既定评分表进行评分，评分项名称和满分不得更改。"
    "你的评语必须符合正式学术评审语言规范。"
    "严禁提及任何文件、材料、图片、PPT、视频、帧率、格式或技术处理过程。"
)

def build_stage2_user_prompt(merged_summaries: str, locked_schema=None) -> str:
    return f"""
请根据以下【作品研究内容摘要】进行综合评分。

【评分项目与满分（总计 100 分，必须严格遵守，不得改名、不得增减）】：
1. 市场契合度（25分）
2. 创新性应用（25分）
3. 技术可行性（20分）
4. 表达能力（15分）
5. 社会价值（15分）

评分与评语要求（必须全部满足）：
1. 评分对象仅限于作品本身的研究内容、应用场景与学术价值
2. 评分项名称必须与上述完全一致（包括中文与括号内分值）
3. 各项评分需体现区分度，避免平均分
4. 总分建议控制在 65–95 分之间，多数作品集中在 75–85 分
5. 评语字数 50–80 字
6. 评语中必须自然出现作品中的具体研究关键词或核心概念
7. 评语中【严禁】出现以下任何词语或同类表达：
   - 文件、文档、材料、图片、PPT、视频、帧、格式、截图、解析、整理
8. 不得描述“观看”“阅读”“呈现方式”或任何技术处理过程
9. 使用 JSON 输出，格式必须严格如下：

{{
  "scores": {{
    "市场契合度(25)": int,
    "创新性应用(25)": int,
    "技术可行性(20)": int,
    "表达能力(15)": int,
    "社会价值(15)": int
  }},
  "comment": "评语正文"
}}

作品研究内容摘要：
----------------
{merged_summaries}
----------------
"""


# =========================================================
# Stage 2 失败兜底：仅生成评语（同样语言强约束）
